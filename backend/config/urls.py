from django.contrib import admin
from django.urls import path, include

from rest_framework.routers import DefaultRouter
from rest_framework_simplejwt.views import (
    TokenObtainPairView,
    TokenRefreshView,
)

from products.views import ProductViewSet, CategoryViewSet, ReviewViewSet
from orders.views import OrderViewSet
from users.views import UserViewSet, RegisterView, MeView
from analytics.views import SalesAnalyticsView

router = DefaultRouter()
router.register(r'products', ProductViewSet, basename='product')
router.register(r'categories', CategoryViewSet, basename='category')
router.register(r'orders', OrderViewSet, basename='order')
router.register(r'reviews', ReviewViewSet, basename='review')
router.register(r'users', UserViewSet, basename='user')

urlpatterns = [
    path('admin/', admin.site.urls),
    path('api/auth/token/', TokenObtainPairView.as_view(), name='token_obtain_pair'),
    path('api/auth/token/refresh/', TokenRefreshView.as_view(), name='token_refresh'),
    path('api/auth/register/', RegisterView.as_view(), name='auth-register'),
    path('api/auth/me/', MeView.as_view(), name='auth-me'),
    path('api/admin/analytics/sales/', SalesAnalyticsView.as_view(), name='sales-analytics'),
    path('api/', include(router.urls)),
]
